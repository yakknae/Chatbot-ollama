      CONCAT('Descripción del producto ', i),
      ROUND(10 + (RAND() * 90), 2),
      ROUND(100 + (RAND() * 400), 2),
      FLOOR(1 + RAND() * 100),
      FLOOR(1 + RAND() * 10000),
      FLOOR(1 + RAND() * 10000),
      CONCAT('CB', LPAD(i, 10, '0'))
    );
