DELIMITER //
CREATE PROCEDURE `insertar_categorias`()
BEGIN
  DECLARE i INT DEFAULT 1;
  WHILE i <= 10000 DO
    INSERT INTO Categorias (nombre, descripcion)
    VALUES (
      CONCAT('Categoria ', LPAD(i, 5, '0')),
      CONCAT('Descripción de la categoría ', i)
    );
    SET i = i + 1;
  END WHILE;
END//
DELIMITER ;

